package com.wintv.lottery.personal.vo;

public class XinshuiVO {
	private String raceName;//联赛
	private String startTime;//开赛时间
	private String hostAndGuest;//主客对阵
	private String ensureMoney;//保证金
	private long buyCnt;//购买人数
	private String price;//价格
	private String result;//结果
	public String getRaceName() {
		return raceName;
	}
	public void setRaceName(String raceName) {
		this.raceName = raceName;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getHostAndGuest() {
		return hostAndGuest;
	}
	public void setHostAndGuest(String hostAndGuest) {
		this.hostAndGuest = hostAndGuest;
	}
	public String getEnsureMoney() {
		return ensureMoney;
	}
	public void setEnsureMoney(String ensureMoney) {
		this.ensureMoney = ensureMoney;
	}
	public long getBuyCnt() {
		return buyCnt;
	}
	public void setBuyCnt(long buyCnt) {
		this.buyCnt = buyCnt;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}

}
